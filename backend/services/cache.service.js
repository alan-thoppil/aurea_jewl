// ============================================
// SIMPLE MEMORY CACHE
// ============================================

const cache = {}

// ============================================
// SET CACHE
// ============================================

export const setCache = (
    key,
    value,
    ttl = 60
) => {

    cache[key] = {

        value,

        expiry:
            Date.now() + ttl * 1000

    }

}

// ============================================
// GET CACHE
// ============================================

export const getCache = (
    key
) => {

    const item = cache[key]

    // ==========================================
    // CACHE MISS
    // ==========================================

    if (!item) {
        return null
    }

    // ==========================================
    // CACHE EXPIRED
    // ==========================================

    if (
        Date.now() > item.expiry
    ) {

        delete cache[key]

        return null

    }

    // ==========================================
    // RETURN CACHE
    // ==========================================

    return item.value

}

// ============================================
// CLEAR CACHE
// ============================================

export const clearCache = (
    key
) => {

    delete cache[key]

}